# 字符串类型
name = "张三"
# 整形
age = 19
# 小数，浮点类型
height = 175.2
# 可以通过type()函数获取当前变量的类型
print(name, type(name))
print(age, type(age))
print("-" * 50)
# 变量运算
a = 5
b = 9
print(a + b)
print(a - b)
print(a * b)
print(a / b)
# 取整除
print(a // b)
# 取余数
print(a % b)
# 幂
print(a ** b)
print("-" * 50)

# 字符串可以直接拼接
username = "sadhfuas"
password = "sajhsfsa"
print(username + password)
# 字符串可以重复n次
print(username * 8)
